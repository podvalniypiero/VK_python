# Маша решила обновить интерьер своей комнаты и поменять на одной из стен обои. У неё есть неограниченное число рулонов ширины W сантиметров. Для определённости Маша клеит обои полосами вида [x,x+W], где x — число сантиметров, начиная от края стены, где начинается полоса обоев, x+W — где заканчивается. По высоте полоса всегда начинается у пола и заканчивается у потолка. Маша уже наклеила обои в некоторых местах,
# координаты x этих участков известны. Ширина стены в сантиметрах составляет L.
# Ваша задача — покрыть все оставшиеся участки стены обоями так, чтобы израсходовать минимальное число полос обоев.
# Формат входных данных
# В первой строке дано число полос, которые Маша успела наклеить,
# N (1≤N≤10^5), ширина стены L (1≤L≤10^18) и ширина рулона обоев W
# (1≤W≤L). Во второй строке даны xi — координаты левых концов наклеенных полос в порядке возрастания (0≤xi<L−W).
# Наклеенные полосы могут пересекаться.
# Выведите единственное число – минимальное число полос, которого хватит на оклейку оставшихся участков стены.


# Функция для нахождения минимального числа полос обоев
def find_min_wallpapers(N, L, W, xi):
    # Создаем список, представляющий участки стены
    wall_segments = []

    # Изначально стена не покрыта обоями
    last_end = 0

    # Определяем участки стены, которые еще не покрыты обоями
    for i in range(N):
        wall_start = xi[i]
        if wall_start > last_end:
            wall_segments.append(wall_start - last_end)
        last_end = max(wall_start + W, last_end)

    # Покрываем последний участок стены до конца
    if last_end < L:
        wall_segments.append(L - last_end)

    # Считаем количество полос обоев
    total_wallpapers = sum([(segment + W - 1) // W for segment in wall_segments])

    return total_wallpapers


# Считываем входные данные
N, L, W = map(int, input().split())
xi = list(map(int, input().split()))

# Вызываем функцию для нахождения минимального числа полос обоев
result = find_min_wallpapers(N, L, W, xi)

print(result)
